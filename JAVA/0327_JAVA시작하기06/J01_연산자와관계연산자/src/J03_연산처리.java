public class J03_연산처리 {
    public static void main(String[] args) {
        //산술연산 출력하기
        System.out.println("_______산술연산 출력하기_______");
        System.out.println("3+5 = " + (3+5));
        System.out.println("3-5 = " + (3-5));
        System.out.println("3*5 = " + (3*5));
        System.out.println("3/5 = " + (3/5));
        System.out.println("3%5 = " + (3%5));


        //실수연산 출력하기
        //둘중에 하나라도 실수이면 결과도 실수가 출력
        System.out.println("_______산술연산 출력하기_______");
        System.out.println("_______둘중하나라도 실수이면 결과도 실수 출력_______");
        System.out.println( "5.0/2.0 = " + (5.0 / 2.0));
        System.out.println( "5/2.0 = " + (5/ 2.0));
        System.out.println( "5.0/2 = " + (5.0 / 2));
        System.out.println( "19.0/4 = " + (19.0 / 4));
        System.out.println( "5.0*4.0 = " + (5.0 * 4.0));


    }
}
